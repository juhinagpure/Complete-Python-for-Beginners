class Employee:
    name = "Harry"
    language = "Python" # class attribute
    salary = 1200000

harry = Employee()
harry.name="Harry" # object attribute
print(harry.name, harry.language, harry.salary)


rohan=Employee()
rohan.name = "Rohan"
print(rohan.name, rohan.language, rohan.salary)

# Here name is object attribute and salary and language are language are class attributes as they directly belong to the class


    
